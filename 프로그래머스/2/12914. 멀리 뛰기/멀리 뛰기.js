function solution(n) {
    // 1칸과 2칸만 가능하므로 피보나치 수열과 유사한 패턴을 가짐
    // n=1 일 때는 1가지 방법: (1)
    // n=2 일 때는 2가지 방법: (1,1), (2)
    // n=3 일 때는 3가지 방법: (1,1,1), (1,2), (2,1)
    // n=4 일 때는 5가지 방법: (1,1,1,1), (1,1,2), (1,2,1), (2,1,1), (2,2)
    
    const MOD = 1234567;
    
    // 기본 케이스
    if (n === 1) return 1;
    if (n === 2) return 2;
    
    // 피보나치 수열을 구하는 방식으로 계산
    let prev = 1; // n=1일 때의 방법 수
    let curr = 2; // n=2일 때의 방법 수
    
    for (let i = 3; i <= n; i++) {
        // 현재 위치의 방법 수 = 이전 위치의 방법 수 + 이전이전 위치의 방법 수
        const next = (prev + curr) % MOD;
        prev = curr;
        curr = next;
    }
    
    return curr;
}